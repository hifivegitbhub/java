package d1015;

public class OpTest3 {

	public static void main(String[] args) {
		int data1 = 10;
		System.out.println("증가연산자");
		System.out.println(++data1);
		System.out.println(++data1);
		System.out.println(++data1);
		
		data1 = 10;
		
		System.out.println("감소연산자");
		System.out.println(--data1);
		System.out.println(--data1);
		System.out.println(--data1);
	}

}
